        <h2>About PasupatiNath (पशुपतिनाथ मन्दिर)</h2>
        <p>
          The Pashupatinath Temple (पशुपतिनाथ मन्दिर) is a Hindu temple dedicated to Pashupati, a form of Shiva. It is located in Kathmandu, Nepal near the Bagmati River. The temple was classified as a World Heritage Site in 1979. This "extensive Hindu temple precinct" is a "sprawling collection of temples, ashrams, images and inscriptions raised over the centuries along the banks of the sacred Bagmati river", and is one of seven monument groups in UNESCO's designation of Kathmandu Valley. It is built on an area of 246 hectares (2,460,000 m2) and includes 518 mini-temples and a main pagoda house.
        </p>

        <h2>History Of PasupatiNath</h2>
        <p>
        The exact date of the temple's construction is uncertain, but the current form of the temple was constructed in 1692 CE.Over time, many more temples have been erected around the two-storied temple, including the Vaishnava temple complex with a Rama temple from the 14th century and the Guhyeshwari Temple mentioned in an 11th-century manuscript.Pashupatinath Temple is the oldest Hindu temple in Kathmandu. It is not known for certain when Pashupatinath Temple was built. But according to Nepal Mahatmaya and Himvatkhanda, the deity here gained great fame there as Pashupati. Pashupatinath Temple's existence is recorded as early as 400 CE. The ornamented pagoda houses the linga of Shiva. There are many legends describing how the temple of Aalok Pashupatinath came into existence here.One legend says that Shiva and Parvati took the form of antelopes in the forest on the Bagmati river's east bank. The gods later caught up with him and grabbed him by one of his horns, forcing him to resume his divine form. The broken horn was worshipped as a linga, but over time it was buried and lost. Centuries later a herdsman found one of his cows showering the earth with milk, and after digging at the site, he discovered the divine linga of Pashupatinath.According to Gopalraj Aalok Vhat, the temple was built by Prachanda Deva, a Licchavi king.
Another chronicle states that Pashupatinath Temple was in the form of Linga shaped Devalaya before Supuspa Deva constructed a five-storey temple of Pashupatinath in this place. As time passed, the temple needed to be repaired and renovated. It is known that this temple was reconstructed by a medieval king named Shivadeva (1099–1126 CE). It was renovated by Ananta Malla adding a roof to it.The main temple complex of Pashupatinath and the sanctum sanctorum was left untouched, but some of the outer buildings in the complex were damaged by the April 2015 Nepal earthquake.
        </p>

        <h2>Architecture</h2>
        <p>This main temple is built in Newari architecture. The two-level roofs are of copper with gold covering. The temple rests on a square base platform with a height of 23m 7 cm from base to pinnacle. It has four main doors, all covered with silver sheets. This temple has a gold pinnacle (peak). Inside are two garbhagrihas: the inner garbhagriha or sanctum sanctorum is where the idol is placed, and the outer sanctum is an open corridor-like space</p>
        <h2>Festivals</h2>
        <p>There are many festivals throughout the year, such as the Maha Shivaratri and the Teej festival. Teej is one of the most celebrated festival at Pashupatinath Temple.</p>
