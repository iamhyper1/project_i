        <h2>About Swayambhu Stupa (स्वयम्भू स्तूप)</h2>
        <p>
        Swayambhunath (स्वयम्भू स्तूप) is an ancient religious complex atop a hill in the Kathmandu Valley, west of Kathmandu city. The Tibetan name for the site means 'Sublime Trees', for the many varieties of trees found on the hill. However, Shingun may be of in Nepal Bhasa name for the complex, Swayambhu, meaning 'self-sprung'.[1] For the Buddhist Newars, in whose mythological history and origin myth as well as day-to-day religious practice Swayambhu occupies a central position, it is the most sacred among Buddhist pilgrimage sites. For Tibetans and followers of Tibetan Buddhism, it is second only to Boudha. Swayambhu is the Hindu name.
        </p>

        <h2>History Of Swayambhu Stupa</h2>
        <p>
        Swayambhu is among the oldest religious sites in Nepal. According to the Gopālarājavaṃśāvalī, it was founded by the great-grandfather of King Mānadeva (464–505 CE), King Vṛsadeva, about the beginning of the fifth century CE. This seems to be confirmed by a damaged stone inscription found at the site, which indicates that King Vrsadeva ordered work done in 640 CE.[3]

However, Emperor Ashoka is said to have visited the site in the third century BCE and built a temple on the hill which was later destroyed.

Although the site is considered Buddhist, the place is revered by both Buddhists and Hindus. Numerous Hindu monarch followers are known to have paid their homage to the temple, including Pratap Malla, the powerful king of Kathmandu, who is responsible for the construction of the eastern stairway in the seventeenth century.[4]

The stupa was completely renovated in May 2010, its first major renovation since 1921[5][6] and its 15th in the nearly 1,500 years since it was built. The Swayambhu Shrine was re-gilded using 20 kg of gold. The renovation was funded by the Tibetan Nyingma Meditation Center of California, and began in June 2008.[7]

At around 5 a.m. on 14 February 2011, Pratapur Temple in the Swayambhu Monument Zone suffered damage from a lightning strike during a sudden thunderstorm.[8]

The Swayambunath complex suffered damage in the April 2015 Nepal earthquake.[9]
        </p>

        <h2>Architecture</h2>
        <p>The stupa consists of a dome at the base, on top of which is a cube structure, painted with eyes of Buddha looking in all four directions. There are pentagonal toranas present above each of the four sides of the cube with Buddha reliefs on them. Behind and above the toranas there are thirteen tiers. Above all the tiers there is a small space above which the Gajur is present.

There are five gilt Buddha shrines at the base of the stupa, all with a Buddha statue inside them. Equally five Tara shrines can be found here, but only four of them are gilt and actually house a Tara statue. The shrine of Vajradhatishori Tara, or White Tara, is empty.[10]</p>
        <h2>Festivals</h2>
        <p>There are many festivals throughout the year, such as the Buddha Jayanti festival.  Buddha Jayanti  is one of the most celebrated festival at Swayambunath Temple.</p>
